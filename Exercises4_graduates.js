function graduates (students) {
    var gradList = {};
    // create object gradList with properties empty;
    for (var i = 0; i < students.length; i++){
        gradList[students[i].class] = '';
    }

    for (var key in gradList){
        var arrGrad = [];
        for (var i = 0; i < students.length; i++){
            var studentGrad = {};
            if (key === students[i].class){
                if (students[i].score >= 75){
                    studentGrad.name = students[i].name;
                    studentGrad.score = students[i].score;
                    arrGrad.push(studentGrad);
                }
            }
        }
        gradList[key] = arrGrad;
    }
    return gradList;
  }
  
